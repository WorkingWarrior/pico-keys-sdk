/* Supported curves table */
static const curve_def_t supported_curves[] = {
    { &secp192r1_params, MBEDTLS_ECP_DP_SECP192R1 },
    { &secp256r1_params, MBEDTLS_ECP_DP_SECP256R1 },
    { &secp384r1_params, MBEDTLS_ECP_DP_SECP384R1 },
    { &secp521r1_params, MBEDTLS_ECP_DP_SECP521R1 },
    { &bp256r1_params, MBEDTLS_ECP_DP_BP256R1 },
    { &bp384r1_params, MBEDTLS_ECP_DP_BP384R1 },
    { &bp512r1_params, MBEDTLS_ECP_DP_BP512R1 },
    { &secp192k1_params, MBEDTLS_ECP_DP_SECP192K1 },
    { &secp256k1_params, MBEDTLS_ECP_DP_SECP256K1 },
    { &curve25519_params, MBEDTLS_ECP_DP_CURVE25519 },
    { &curve448_params, MBEDTLS_ECP_DP_CURVE448 },
    { NULL, MBEDTLS_ECP_DP_NONE }
};

mbedtls_ecp_group_id ec_get_curve_from_prime(const uint8_t *prime, size_t prime_len) {
    if (!prime || prime_len == 0) {
        return MBEDTLS_ECP_DP_NONE;
    }

    for (const curve_def_t *curve = supported_curves; curve->params != NULL; curve++) {
        if (prime_len == curve->params->len && 
            memcmp(prime, curve->params->prime, prime_len) == 0) {
            return curve->id;
        }
    }
    return MBEDTLS_ECP_DP_NONE;
}

const curve_params_t* ec_get_curve_params(mbedtls_ecp_group_id curve_id) {
    for (const curve_def_t *curve = supported_curves; curve->params != NULL; curve++) {
        if (curve->id == curve_id) {
            return curve->params;
        }
    }
    return NULL;
}

bool ec_is_curve_supported(mbedtls_ecp_group_id curve_id) {
    return ec_get_curve_params(curve_id) != NULL;
}
